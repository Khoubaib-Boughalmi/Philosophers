#include "philosphers.h"

void	ft_bzero(void *s, size_t n)
{
    size_t  i;

    i = 0;
    if (!n)
		return ;
    while (i < n)
    {
        ((unsigned char *)s)[i] = '\0';
        i++;
    }
}